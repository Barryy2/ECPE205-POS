public class Orders {
    String Sku, name, price ,qty , amount;

    public Orders(String sku, String name, String price, String qty, String amount) {
        Sku = sku;
        this.name = name;
        this.price = price;
        this.qty = qty;
        this.amount = amount;
    }

    public String getSku() {
        return Sku;
    }

    public void setSku(String sku) {
        Sku = sku;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }
}
